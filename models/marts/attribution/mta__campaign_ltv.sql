-- mta__campaign_ltv.sql
-- MTA-Attributed LTV per Campaign
-- Applies fractional MTA credit to device-level LTV windows
-- Compare LTV across 6 attribution models × 6 LTV windows
--
-- Grain: One row per AD_PARTNER + CAMPAIGN_ID + PLATFORM + DATE

{{
    config(
        materialized='incremental',
        unique_key=['AD_PARTNER', 'CAMPAIGN_ID', 'PLATFORM', 'DATE'],
        incremental_strategy='merge',
        on_schema_change='append_new_columns',
        tags=['mta', 'attribution', 'ltv', 'mart']
